package io.transwarp.bean;

public class YxSupport {
	private String id;
	private String theme;
	private String name;
	private String proDescrible;
	private String operator;
	private String result;
	private String startTime;
	private String firstResponseTime;
	private String lastResponseTime;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTheme() {
		return theme;
	}
	public void setTheme(String theme) {
		this.theme = theme;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getProDescrible() {
		return proDescrible;
	}
	public void setProDescrible(String proDescrible) {
		this.proDescrible = proDescrible;
	}
	public String getOperator() {
		return operator;
	}
	public void setOperator(String operator) {
		this.operator = operator;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getFirstResponseTime() {
		return firstResponseTime;
	}
	public void setFirstResponseTime(String firstResponseTime) {
		this.firstResponseTime = firstResponseTime;
	}
	public String getLastResponseTime() {
		return lastResponseTime;
	}
	public void setLastResponseTime(String lastResponseTime) {
		this.lastResponseTime = lastResponseTime;
	}
	
	
}
